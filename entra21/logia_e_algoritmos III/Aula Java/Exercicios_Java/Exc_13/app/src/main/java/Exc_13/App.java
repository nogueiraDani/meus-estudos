package Exc_13;

public class App {

    public static void main(String[] args) {

        int valor = -2;

        if (valor == 10) {
            System.out.println(valor + " e igual a 10");
        } else if (valor > 10) {
            System.out.println(valor + " e maior que 10");
        } else {
            System.out.println(valor + " e menor que 10");
        }
    }
}
