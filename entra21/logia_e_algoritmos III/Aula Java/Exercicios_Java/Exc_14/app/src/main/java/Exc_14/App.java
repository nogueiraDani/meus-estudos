package Exc_14;

public class App {

    public static void main(String[] args) {

        int primeiroValor = 12;
        int segundoValor = 8;

        if (primeiroValor == segundoValor) {
            System.out.println(primeiroValor + " e igual a " + segundoValor);
        } else if (primeiroValor > segundoValor) {
            System.out.println(primeiroValor + " e maior que " + segundoValor);
        } else {
            System.out.println(primeiroValor + " e menor que " + segundoValor);
        }

    }
}
